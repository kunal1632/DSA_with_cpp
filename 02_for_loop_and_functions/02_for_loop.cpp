#include <iostream>
using namespace std;

int main()
{
    int n;
    cout << "enter the value of n: ";
    cin >> n;
    cout << "printing the count from 1 to " << n << endl;
    for (int i = 1; i <= n; i++)
    {
        cout << i << endl;
    }

    for (int a = 0, b = 1, c = 5; a < 5, b<5, c> 0; a++, b++, c--)
    {
        cout << a << " " << b << " " << c << endl;
    }
}